-- SCRIPT TO INITIALIZE CHRONIC MEDICATION OUTREACH SYSTEM IN SUPABASE
-- COPY AND PASTE THIS SCRIPT INTO SUPABASE SQL EDITOR TO SETUP TABLES AND RELATIONSHIPS

-- 1. Pharmacies / Tenants Table
CREATE TABLE IF NOT EXISTS pharmacies (
    pharmacy_id TEXT PRIMARY KEY,
    pharmacy_name TEXT NOT NULL,
    address TEXT NOT NULL,
    phone_number TEXT NOT NULL,
    color_theme TEXT NOT NULL DEFAULT 'teal',
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 2. Patients Table
CREATE TABLE IF NOT EXISTS patients (
    patient_id TEXT PRIMARY KEY,
    pharmacy_id TEXT REFERENCES pharmacies(pharmacy_id) ON DELETE CASCADE,
    full_name TEXT NOT NULL,
    phone_number TEXT NOT NULL,
    chronic_condition TEXT NOT NULL, -- 'Hypertension', 'Diabetes', 'HIV/ARVs', 'Asthma', 'Epilepsy', 'Chronic Kidney Disease', 'Tuberculosis (TB)', 'Heart Failure', 'Depression/Mental Health', 'Other'
    preferred_channel TEXT NOT NULL DEFAULT 'WhatsApp', -- 'WhatsApp', 'SMS', 'Both'
    status TEXT NOT NULL DEFAULT 'Active', -- 'Active', 'Inactive'
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 3. Medications Table
CREATE TABLE IF NOT EXISTS medications (
    medication_id TEXT PRIMARY KEY,
    patient_id TEXT REFERENCES patients(patient_id) ON DELETE CASCADE UNIQUE,
    medication_name TEXT NOT NULL,
    dosage TEXT NOT NULL,
    duration_days INTEGER NOT NULL DEFAULT 30,
    last_refill_date TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    next_refill_date TIMESTAMP WITH TIME ZONE NOT NULL
);

-- 4. Reminders / Outbox Logs
CREATE TABLE IF NOT EXISTS reminders (
    reminder_id TEXT PRIMARY KEY,
    patient_id TEXT REFERENCES patients(patient_id) ON DELETE CASCADE,
    medication_id TEXT REFERENCES medications(medication_id) ON DELETE CASCADE,
    reminder_date TIMESTAMP WITH TIME ZONE NOT NULL,
    channel TEXT NOT NULL, -- 'WhatsApp', 'SMS'
    status TEXT NOT NULL DEFAULT 'Pending', -- 'Sent', 'Failed', 'Pending'
    message TEXT NOT NULL,
    sent_at TIMESTAMP WITH TIME ZONE,
    category TEXT NOT NULL -- e.g. '7 days until refill alert', '3 days...', 'Due Today', etc.
);

-- 5. Personalized Message Templates (Stored per Condition per Pharmacy)
CREATE TABLE IF NOT EXISTS templates (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    pharmacy_id TEXT REFERENCES pharmacies(pharmacy_id) ON DELETE CASCADE UNIQUE,
    hypertension TEXT NOT NULL,
    diabetes TEXT NOT NULL,
    hiv_arvs TEXT NOT NULL,
    general TEXT NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 6. Feedback Table (Patient Feedback Mechanism)
CREATE TABLE IF NOT EXISTS feedback (
    feedback_id TEXT PRIMARY KEY,
    patient_id TEXT REFERENCES patients(patient_id) ON DELETE CASCADE,
    rating INTEGER NOT NULL CHECK (rating >= 1 AND rating <= 5),
    comment TEXT NOT NULL,
    category TEXT NOT NULL, -- 'Reminders', 'Refills', 'Pharmacy Service', 'Other'
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 7. Progress Metrics Table (Patient Progress Monitoring)
CREATE TABLE IF NOT EXISTS progress_metrics (
    metric_id TEXT PRIMARY KEY,
    patient_id TEXT REFERENCES patients(patient_id) ON DELETE CASCADE,
    systolic_bp INTEGER,
    diastolic_bp INTEGER,
    blood_sugar REAL,
    sugar_type TEXT, -- 'Fasting', 'Random', 'Post-Meal'
    wellness_level TEXT NOT NULL, -- 'Great', 'Good', 'Fair', 'Poor'
    symptoms TEXT,
    logged_date TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 8. Appointments Table (Appointment Booking System)
CREATE TABLE IF NOT EXISTS appointments (
    appointment_id TEXT PRIMARY KEY,
    patient_id TEXT REFERENCES patients(patient_id) ON DELETE CASCADE,
    doctor_name TEXT NOT NULL,
    appointment_date TIMESTAMP WITH TIME ZONE NOT NULL,
    reason TEXT NOT NULL,
    status TEXT NOT NULL DEFAULT 'Scheduled', -- 'Scheduled', 'Completed', 'Cancelled'
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 9. Consultations Table (Patient-Doctor Clinical Q&A Desk)
CREATE TABLE IF NOT EXISTS consultations (
    consultation_id TEXT PRIMARY KEY,
    patient_id TEXT REFERENCES patients(patient_id) ON DELETE CASCADE,
    question TEXT NOT NULL,
    answer TEXT,
    answered_at TIMESTAMP WITH TIME ZONE,
    status TEXT NOT NULL DEFAULT 'Pending', -- 'Pending', 'Answered'
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

